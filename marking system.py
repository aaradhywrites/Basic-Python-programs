marks = float(input("Enter student's marks: "))
 
if marks >= 90:
    print("Your Grade is : A" "Excellent performance.")
elif marks >= 80:
    print("Your Grade is : B" "Good performance.")
elif marks >= 70:
    print("Your Grade is : C" "Good performance but can be improved.")
else:
    print("Your Grade is : D" "Improvement is needed in your performance.")